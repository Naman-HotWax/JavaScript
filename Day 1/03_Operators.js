var num1 = 2;
var num2 = 9;

// Arithmetic operators in action in JavaScript
console.log("The value of num1 + num2 is " + (num1 + num2)); //11
console.log("The value of num1 - num2 is " + (num1 - num2)); //-7
console.log("The value of num1 * num2 is " + (num1 * num2)); //18
console.log("The value of num1 / num2 is " + (num1 / num2)); //0.2222
console.log("The value of num1 ** num2 is " + (num1 ** num2)); //512
console.log("The value of num1++ is " + (num1++)); //2
console.log("The value of ++num1 is " + (++num1)); //4
console.log("The value of num1-- is " + (num1--)); //4
console.log("The value of --num1 is " + (--num1)); //2
